// Helper functions for filtering and sorting
export const getWaterfallsByProvince = (province: string) => {
  return waterfallLocations.filter(waterfall => waterfall.province === province);
};

export const getTop10Waterfalls = () => {
  return waterfallLocations
    .sort((a, b) => (b.height || 0) - (a.height || 0))
    .slice(0, 10);
};

export const getWaterfallsByDifficulty = (difficulty: string) => {
  return waterfallLocations.filter(waterfall => waterfall.difficulty === difficulty);
};

export const getAllProvinces = () => {
  return [...new Set(waterfallLocations.map(w => w.province))].sort();
};

export const getWaterfallBySlug = (slug: string) => {
  return waterfallLocations.find(w => w.id === slug);
};
